        <br>
        气郁质——乳腺癌
        <br>
        气郁体质多因长期情绪不畅郁结造成。女性易患宫颈癌、乳腺癌。男性易患肝癌、胃癌、食道癌。
        <br>
        气郁体质的人往往忧郁脆弱、敏感多疑、神情郁闷、胸胁胀满，易患郁症，脏躁，不寐，梅核气，惊恐等病症。久而久之极易患上乳腺癌。
        <br>
        调理方法：多交流，好心情
        <br>
        多吃小麦、蒿子杆、葱、蒜、海带、海藻、萝卜、金橘、山楂等具有行气、解郁、消食、醒神作用的食物。
        <br>
        睡前避免饮茶、咖啡等提神醒脑的饮料。
        <br>
        体质强壮的可尽量增加户外活动，可坚持较大量的运动锻炼，如跑步、登山、游泳、武术等。
        <br>
