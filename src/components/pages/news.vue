        <br>
        痰湿质——肠癌
        <br>
        痰湿体质的人体形肥胖，喜食肥甘醇酒，舌体胖大，舌苔白腻，多因寒湿侵袭、饮食不节，先天禀赋、年老久病、缺乏运动而发病。是肠癌的好发人群。
        <br>
