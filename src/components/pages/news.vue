        <br><br>
        3、晚餐吃“产气”食物
        <br><br>
        晚上吃一些在消化过程中，会产生较多气体的食物。如豆类、包心菜、绿椰菜、青椒、茄子、土豆、芋头、玉米、香蕉、面包、柑橘类水果和添加木糖醇(甜味剂)的饮料及甜点等，会让人产生腹胀感，妨碍正常睡眠。
        <br><br>
        4、晚餐吃剩饭
        <br><br>
        勤俭节约是美德，于是不少的人害怕浪费常常会将中午的剩饭留到晚上吃，但是，根据研究统计发现，很多胰腺炎尤其是急性胰腺炎的发病，都与不健康的饮食习惯有关，例如有人经常暴饮暴食，或者吃一些变质的食物，尤其是剩下的鱼和肉千万不能吃。这类食物蛋白质含量高，可能导致细菌感染，进一步诱发胰腺病变。
        <br><br>
        5、晚餐后过量饮酒
        <br><br>
        有人喜欢睡前喝点酒帮助睡眠，也有人是迫于晚上应酬不得不喝酒，但无论哪种情况，都应该尽量杜绝。睡前饮酒，会使酒中的很多有害物质在体内存积，毒害身体，损伤视网膜，降低抵抗力，并使打鼾和睡眠呼吸暂停综合征明显加重。
