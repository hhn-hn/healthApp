        气虚质——肺癌
        <br>
        元气不足，气息及脏腑功能低弱是气虚质的主要特征，常见于过度劳累的人。
        <br>
        一般表现为面白少华，气短懒言，易出汗，食少，易疲乏，舌淡红，舌体胖大，脉虚缓。
        <br>
        这类人机体免疫力低，易感冒，病后迁延不愈。
        <br>
        调理方法：睡好觉，适当运动
        <br>
        多吃具有益气健脾作用的食物，如黄豆、白扁豆、鸡肉、泥鳅、香菇、大枣、桂圆、蜂蜜等。
        <br>
        少食具有耗气作用的食物，如槟榔、空心菜、生萝卜等。
        <br>
        以柔缓运动，散步、打太极拳等为主，不宜做大负荷运动和出大汗的运动，忌用猛力和长久憋气。
        <br>
        食疗：山药薏米芡实粥
        <br>
        用干怀山药500克，然后把怀山药、薏苡仁、芡实拿到加工粮食的地方请他们帮忙打成粉末。
        <br>
        做好以后，每天取一或者两调羹，放入小米，或者大米中，熬粥，每天喝一次，坚持一个月。
        <br>
        阳虚质——胃癌
        <br>
        阳虚体质为先天阳气不足及后天贪凉导致。机体免疫力低，患癌风险增大。
        <br>
        阳虚体质的人面色多淡白，怕寒冷，大便稀溏、夜尿清长，得病易发高热，耐冬不耐夏，特别是贪寒凉的人易得胃癌。
        <br>
        调理方法：少吃凉，要保暖
        <br>
        可适当吃些“发物”（甘温益气），比如牛羊狗肉、葱、姜、蒜、花椒、鳝鱼、韭菜、辣椒、胡椒等。
        <br>
        少食生冷寒凉食物如黄瓜、藕、梨、西瓜等。
        <br>
        秋冬注意保暖，尤其是足下、背部及下腹部丹田部位的防寒保暖。夏季避免吹空调电扇。
        <br>
        可做一些舒缓柔和的运动，或自行按摩气海、足三里、涌泉等穴位，可适当洗桑拿、温泉浴。
        <br>
        食疗：姜桂枝粥
        <br>
        干姜、炙甘草各3克，桂枝10克，肉苁蓉15克，核桃肉30克，粳米100克，洗干净后，加入适量水在火上煮，先大火，水烧开后，再用小火炖半个小时左右就可以了，每天吃一两碗。
        <br>
        阴虚质——肺癌
        <br>
        阴虚体质起因于体内津液等阴液亏少。吸烟30年以上容易肺阴亏虚，易患肺癌。经常眼干目涩、急躁易怒则为肝阴虚。
        <br>
        阴虚体质的人体型消瘦、口燥咽干、便秘尿黄、手足心热、易烦躁，由于人体过热，阴液过度“蒸发”，导致阴液不足，也容易得肺癌。
        <br>
        调理方法：忌辛辣，多滋润
        <br>
        多吃甘凉滋润的食物，比如瘦猪肉、鸭肉、龟、鳖、绿豆、冬瓜、芝麻、百合等。
        <br>
        少食羊肉、狗肉、韭菜、辣椒、葱、蒜、葵花子等性温燥烈的食物。
        <br>
        中午保持一定的午休时间。避免熬夜、剧烈运动和在高温酷暑下工作。
        <br>
