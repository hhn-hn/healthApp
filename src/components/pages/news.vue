        <br><br>
        二、晚餐食用大量蛋白质
        <br><br>
        蛋白质是制造血液或肌肉的重要营养成分。这种组织在睡觉时产生，所以在晚餐上摄取蛋白质。在体内处理蛋白质消耗很大热量，所以为了使身体变得苗条，也应积极摄取肉类。
        <br><br>
        三、清淡晚餐
        <br><br>
        晚餐宜清淡，注意选择脂肪少、易消化的食物，且注意不应吃得过饱。晚餐营养过剩，消耗不掉的脂肪就会在体内堆积，造成肥胖，影响健康。晚餐最好选择：面条、米粥、鲜玉米、豆类、素馅包子、小菜、水果拼盘。偶尔在进餐的同时饮用一小杯加饭酒或红酒也很好。
