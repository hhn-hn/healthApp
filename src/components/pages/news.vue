        <br><br>
        很多人喜欢跑步，但是跑步的人们也分为两类。一类人选择空腹奔跑，另一类人选择吃饱后跑步。空腹跑步的人认为空着肚子跑步可以消除更多的脂肪，没吃东西之前体内没有糖分，可以消耗掉更多的脂肪。饱腹跑步的人认为吃完东西再跑步会消耗更多，效果也更好。一般坚持跑步三个月能达到健身的效果，那么在空腹跑步和饱腹跑步三个月后有多大差异的？
        <br><br>

        身体代谢差异
        <br>
        在健身的过程中，我们经常听到这样一个词，即基础代谢。健身教练总是告诉你基础代谢的重要性，让你能轻松减肥。那究竟什么是基础代谢呢？这其实是人们在静止状态下维持生命所需的基本能量，肌肉含量的高低是可以影响代谢的。空着肚子跑步会减少肌肉质量，并减少基础代谢。早上我们的身体经过一晚上运作后已经消耗完了，此时的身体无法在跑步的时候提供糖分，因此就会选择分解肌肉的含量来提供能量。而吃完东西再跑步的人热量充足，跑步时就会先消耗热量。因此，三个月下来，空腹跑步的人的代谢低于饱后跑步的人。
        <br><br>

        体重的差异
        <br>
        事实上，空腹跑步并不会很快减轻体重。当我们空腹运动时，我们会失去更多的肌肉。空腹跑步三个月后，身体重量也有减少，但是在这些失去的重量中，消耗的肌肉比脂肪多。因此当我们停止空腹跑步动时，体重很快就会反弹回来。吃饱后再跑步就不会有这个情况，但是如果吃太多热量的话，在跑步过程中没有被消耗完，你的体重很可能没有减少，还增加了不少。
        <br><br>

